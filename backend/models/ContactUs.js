const mongoose = require('mongoose');

const ContactUsSchema = new mongoose.Schema({
  fullName: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  phoneNumber: {
    type: String,
    required: true
  },
  inquiryAbout: {
    type: String,
    required: true
  },
  message: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

const ContactUs = mongoose.model('ContactUs', ContactUsSchema);

module.exports = ContactUs;
